package mipl.livechat;

public class PojoMsg {
    String id, msg, meta_msg, time, chat_id, user_id, name_support;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getMeta_msg() {
        return meta_msg;
    }

    public void setMeta_msg(String meta_msg) {
        this.meta_msg = meta_msg;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getChat_id() {
        return chat_id;
    }

    public void setChat_id(String chat_id) {
        this.chat_id = chat_id;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getName_support() {
        return name_support;
    }

    public void setName_support(String name_support) {
        this.name_support = name_support;
    }
}
